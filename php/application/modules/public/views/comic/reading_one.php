		khiến anh chú ý nhất là đôi môi đỏ mọng của cô chỉ cách khóa quần anh
		có 5cm. Bây giờ mà có ai bước vào, nhìn thấy cảnh này chắc chắn sẽ
		nghĩ đến… Bạch Lập Nhân hóa đá, không dám nhúc nhích. Mười phút sau,
		thấy cô ngủ rất “ngoan ngoãn”, Bạch Lập Nhân mới lấy tay đẩy nhẹ đầu
		cô ra. Đầu Diệu Diệu như quả bóng da, lăn qua lăn lại rồi tiếp tục
		chôn mặt vào đùi anh. Bạch Lập Nhân đang định đứng dậy, bế cô về
		phòng. Nào ngờ “rầm” một tiếng, cửa nhà anh và nhà cô vốn đang mở
		rộng, vậy mà cùng lúc bị gió thổi đóng sập vào. Bạch Lập Nhân trố mắt,
		vội vàng lay: “Diệu Diệu!” Làm sao bây giờ?! “Hửm…” Diệu Diệu bị “làm
		phiền” thì cực kỳ không thoải mái, cô mơ mơ màng màng ngồi xuống đẩy
		tay anh, lại choáng váng day day thái dương. Anh thở dài nhẹ nhõm:
		“Này, cô có cầm chìa khóa nhà theo không?” Thật ra hỏi cũng bằng thừa,
		cô đang mặc váy liền, nhìn là biết có cầm chìa khóa rồi mới ra khỏi
		nhà. Không biết Diệu Diệu có nghe Bạch Lập Nhân nói gì không, ngẩn ngơ
		mất vài giây rồi lại lảo đảo đi về phía anh. Anh vươn tay giữ vai cô
		lại. Diệu Diệu đang vội vàng tìm chỗ ngủ, thấy thế thì khó chịu đẩy
		tay anh ra. Tưởng cô có thể tự đi được, Bạch Lập Nhân vừa buông lỏng
		vai Diệu Diệu ra thì cô lại bổ chửng về hướng anh, không kịp chuẩn bị,
		Bạch Lập Nhân ngã xuống sàn. Rốt cuộc cũng tìm được “gối” rồi. Diệu
		Diệu thoải mái thay đổi tư thế, cố định trên khuôn ngực rắn chắc của
		Bạch Lập Nhân, tiếp tục ngủ. Bạch Lập Nhân chấn động. Khuôn mặt cô lúc
		này đỏ bừng, trông không khác gì một đóa hoa đào xinh đẹp, đẹp đến mức
		khiến người khác phải nín thở. Bạch Lập Nhân muốn lay Diệu Diệu, lại
		kìm lòng không đậu khẽ vỗ về hai má cô, sau đó xẹt qua trán, mi, mắt,
		mũi, cuối cùng, ngón cái anh lưu luyến vuốt ve cánh môi đỏ mọng quyến
		rũ của cô. Anh, đã từng hôn đôi môi này, hiện tại lại rất muốn đến gần
		cô. Bạch Lập Nhân nghĩ nếu không gặp Diệu Diệu một thời gian thì loại
		cảm giác này sẽ dần dần chìm xuống, nào ngờ chỉ mới nhìn thấy cô ở
		ngoài cửa, anh đã biết bản thân chỉ không ngừng tự trấn an, chết lặng
		đè nén khát vọng được ở gần cô xuống. Khuôn mặt đang được anh nhẹ
		nhàng vuốt ve đột nhiên giật giật, mơ màng mở mắt, sau đó nâng mắt
		nhìn anh. Tay anh vẫn còn đặt trên môi cô. Anh bối rối, gương mặt tuấn
		tú đột nhiên đỏ bừng. Đây là lần đầu tiên một kẻ kiêu ngạo như Bạch
		Lập Nhân vụng trộm làm chuyện gì đó mà bị người khác bắt gặp. Anh đang
		định bày ra bộ dạng của một chính nhân quân tử, thuận tiện nghĩ ra một
		cái cớ nào đó để tự bào chữa cho bản thân. Nào ngờ chưa kịp nói gì,
		Diệu Diệu đã mở to mắt, thấy người trước mặt là Bạch Lập Nhân thì chậm
		rãi thả lỏng cơ thể đang căng thẳng, sau đó rũ mắt xuống, tin cậy cọ
		cọ vài cái rồi lại tiếp tục gối đầu lên ngực anh. Cái cô này, tửu
		lượng quá kém! Bạch Lập Nhân bỗng chốc dở khóc dở cười, nhưng trong
		lòng lại thấy ngổn ngang vô cùng. Thà rằng cô cứ hét chói tai đi! Bởi
		vì, Bạch Lập Nhân đột nhiên ngộ ra một sự thật vô cùng phũ phàng, đó
		là Diệu Diệu căn bản không hề coi anh là đàn ông! Trong tiềm thức của
		Diệu Diệu, anh là người mà cô có thể yên tâm đối xử như bạn bè, có khả
		năng hiện tại đã được thăng chức lên xấp xỉ người thân. Ngón tay đang
		vuốt ve môi cô chậm rãi buông xuống. Mặc dù cô đang nằm trong lòng
		anh, nhưng điều đó cũng không giúp anh cảm thấy khá hơn chút nào.

		Trong nháy mắt, một chấp niệm bỗng dưng trào lên trong trái tim Bạch
		Lập Nhân. Anh muốn có cô! Tuyệt đối không thể để cô rơi vào tay Tiết
		Hồ Ly! Đã nhiều ngày, chỉ cần nhắm mắt là anh lại thấy cảnh tượng Tiết
		Hồ Ly và Diệu Diệu dịu dàng thân mật, Bạch Lập Nhân thực sự thấy khó
		chịu. Anh cố tình chơi game để dời đi lực chú ý, nhưng có chơi đến mức
		nào, hay thậm chí còn hút tận mấy bao thuốc lá mà trước giờ mình chưa
		hề động vào cũng không có tác dụng. Nếu, nếu hiện tại anh quyết định
		đập chậu cướp hoa* thì liệu có bao nhiêu phần trăm chiến thắng? Đúng!
		Đập chậu cướp hoa đi! Dựa vào cái gì mà để tên Tiết Hồ Ly kia được lợi
		chứ?! *Nguyên gốc là “hoành đao đoạt ái” – sử dụng vũ lực để cướp lấy
		tình yêu. Nhưng mà… Bạch Lập Nhân thở dài, hơi dịch người, ngửa đầu
		tựa vào chân ghế sô pha, anh tức giận, bời vì, anh buồn rầu. Rốt cuộc
		phải theo đuổi kiểu gì mới đúng đây? Hơn nữa, anh theo đuổi Diệu Diệu?
		Ý tưởng này cũng quá đáng sợ đi, rất xấu hổ! Lỡ như thất bại, lỡ như
		bị từ thối thì lại càng xấu hổ? Hơn nữa, lại còn tặng không cho Tiết
		Hồ Ly một cơ hội dẫm nát mặt anh! Tỉ lệ bị từ chối quá cao, dù sao ánh
		mắt lúc Diệu Diệu nhìn hắn cũng rất si mê. Cắm đầu chạy theo một đoạn
		tình cảm không dám nắm chắc cơ hội, kết cục chỉ có thể từ thảm đến rất
		thảm. Ánh mắt Bạch Lập Nhân cứ giãy dụa mãi. Nhưng… Bạch Lập Nhân nhìn
		lên vách tường, vì ánh sáng từ ngọn đèn chiếu vào mà bóng hai người in
		rõ trên đó. Cái bóng của hai người hiện giờ đang quấn quít lấy nhau,
		gần như hợp thành một thể, mỗi lần anh hít thở, “con tôm nhỏ” đang im
		lặng gối đầu lên ngực anh cũng nhẹ nhàng phập phồng, tất cả tạo lên
		một bức tranh vô cùng hài hòa. Đêm nay, thật sự chỉ là một sự sai lầm
		sau khi say rượu thôi sao? Bạch Lập Nhân muốn lại gần cô thêm chút
		nữa, nhưng cũng chỉ có thể tiếp tục duy trì một khoảng cách an toàn.

		Vì anh biết, nếu bây giờ mình có bất cứ hành động nào, thì đó đều
		không phải quang minh chính đại. Trừ khi… Có thể nào để loại “sai lầm”
		này tiếp tục hay không? Anh biết rõ, nếu bản thân còn tiếp tục duy trì
		sự kiêu ngạo, vẫn không thể nào buông bỏ tư thái ngạo mạn thì sai lầm
		vĩnh viễn vẫn chỉ là sai lầm, mà nếu… anh dám đánh cược. Hơn nữa, Diệu
		Diệu cũng có để ý đến anh, nếu không cô cũng sẽ không chạy đến đây, sẽ
		không nịnh bợ, hy vọng khôi phục lại tình bạn với anh. Có lẽ mọi
		chuyện vẫn chưa kết thúc. Cái ôm đêm nay khiến anh nhận rõ, bản thân
		không hy vọng khoảnh khắc này chỉ tồn tại ngắn ngủi đến như vậy. Nhìn
		cô khẽ nhếch đôi môi đỏ mọng hít thở, toàn tâm toàn ý ỷ lại anh… Nếu,
		nếu lúc này đổi lại là Tiết Hồ Ly đang ở bên cạnh cô thì anh phải làm
		sao? Bạch Lập Nhân phát hiện trái tim mình như đang có hàng vạn con
		kiến thi nhau đục khoét. Không được! Bạch Lập Nhân rốt cuộc cũng hạ
		quyết tâm, lần này, dù bất cứ giá nào, anh cũng phải có được cô! Không
